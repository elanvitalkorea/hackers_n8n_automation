-- 1. surveys 테이블 생성
-- PRD-SurveyApp.md 7.1. 테이블: surveys 스키마 기반
-- 실행 전 기존 테이블이 있다면 삭제합니다.
DROP TABLE IF EXISTS surveys;

CREATE TABLE surveys (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  created_at timestamptz DEFAULT now(),
  name text NOT NULL,
  phone text NOT NULL,
  company_email text NOT NULL,
  company_name text NOT NULL,
  position text NOT NULL,
  company_size text NOT NULL,
  satisfaction smallint NOT NULL CHECK (satisfaction >= 1 AND satisfaction <= 10),
  comment text
);

-- 2. RLS (Row Level Security) 설정
-- Supabase는 기본적으로 RLS가 활성화되어 있습니다.
-- 익명 사용자(웹사이트 방문자)가 설문을 제출(INSERT)하고
-- 통계 페이지에서 데이터를 조회(SELECT)할 수 있도록 정책을 활성화합니다.
ALTER TABLE surveys ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Public anonymous inserts" 
ON surveys FOR INSERT 
WITH CHECK (true);

CREATE POLICY "Public anonymous reads" 
ON surveys FOR SELECT 
USING (true);


